def apply_rotations(images, rotations=[0, 90, 180, 270],stack=False):
    """
        Applies 4 rotations of 0, 90, 180, 270 degrees

        Parameters

        images:
            torch tensor, shape (B, 3, H, W)

        Returns
        rotated_images:
            torch tensor, shape (4*B, 3, H, W)

    """
    assert len(rotations) > 0, "one rotation is needed"
    for rot in rotations:
        assert rot in [0, 90, 180, 270], "only rotations 0, 90, 180, 270"

    rotated_images = []

    n_rot = 0
    if 0 in rotations:
        rotated_images.append(images)
        n_rot += 1
    if 90 in rotations:
        rotated_images.append(images.transpose(2,3).flip(2))
        n_rot += 1
    if 180 in rotations:
        rotated_images.append(images.flip(2,3))
        n_rot += 1
    if 270 in rotations:
        rotated_images.append(images.transpose(2,3).flip(3))
        n_rot += 1

    B = images.size(0)
    targets = images.new_zeros(n_rot*B, dtype=torch.long)
    for i in range(1, n_rot):
        targets[i*B:(i+1)*B] = i

    if stack:
        return torch.stack(rotated_images, 0), targets
    return torch.cat(rotated_images, 0), targets


def get_mean_and_std(dataset):
    '''Compute the mean and std value of dataset.'''
    dataloader = torch.utils.data.DataLoader(dataset, batch_size=1, shuffle=True, num_workers=2)
    mean = torch.zeros(3)
    std = torch.zeros(3)
    print('==> Computing mean and std..')
    for inputs, targets in dataloader:
        for i in range(3):
            mean[i] += inputs[:,i,:,:].mean()
            std[i] += inputs[:,i,:,:].std()
    mean.div_(len(dataset))
    std.div_(len(dataset))
    return mean, std

def init_params(net):
    '''Init layer parameters.'''
    for m in net.modules():
        if isinstance(m, nn.Conv2d):
            init.kaiming_normal(m.weight, mode='fan_out')
            if m.bias:
                init.constant(m.bias, 0)
        elif isinstance(m, nn.BatchNorm2d):
            init.constant(m.weight, 1)
            init.constant(m.bias, 0)
        elif isinstance(m, nn.Linear):
            init.normal(m.weight, std=1e-3)
            if m.bias:
                init.constant(m.bias, 0)


